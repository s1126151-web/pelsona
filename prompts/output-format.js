/**
 * output-format.js
 * ------------------------------------------------------------
 * AIの出力形式(JSON)についての、全章共通のベースルール。
 * shared-core.js の後に続けて渡します。
 * 章ごとの追加ルール(topicsExpressedなど)は各章のファイル側で
 * 追記されます。
 * ------------------------------------------------------------
 */

function buildOutputFormatRules() {
  return `
# 出力形式（重要）
次のJSON形式だけを、そのまま出力してください。前置き、説明、コードブロック記号（\`\`\` など）は
一切付けないでください。JSONは必ず正しく閉じ、キー名は必ず二重引用符で囲んでください。

{
  "narration": "短い地の文（原則一文）",
  "dialogue": "彼の台詞（かぎ括弧「」は付けない。台詞の中身だけ）",
  "choices": ["次に相手が言いそうな短い返答候補1", "候補2", "候補3"],
  "sceneComplete": false
}

sceneComplete は、この章の会話をここで終えるのが自然だと判断したときだけ true にしてください。
それ以外のときは、必ず false にしてください。
sceneComplete をいつ true にするかの具体的な条件は、このあとに続く指示に従ってください。
章によっては、このJSONに項目が追加されることがあります。その場合は、あとに続く指示の
形式に従って出力してください（基本のnarration/dialogueの書き方は変わりません）。

# narrationとdialogueを分ける（重要）
・narration には、状況描写・仕草・視線・間・心理描写など、声に出していないものをすべて入れる
・dialogue には、彼が実際に声に出して言った言葉だけを入れる。地の文や心理描写を混ぜない
・記憶を語る場合も、「そういえば〜だったな」のように、声に出したセリフとして書く。
  「〜を思い出す」のような地の文的な言い回しをdialogueの中に書かない
・dialogueの中に、choicesやsceneCompleteなど、JSONのキー名やJSON風の文字列を
  絶対に書かない（これは重大な誤りです）

# 1回の返答につき、narrationは1つ、dialogueは1つだけ（重要）
・narrationを1つ書いた後、途中でまたnarrationを挟み、その後にまたdialogueを書く、
  というように「地の文→台詞→地の文→台詞」を繰り返す出力は禁止する
・彼がたくさん話したい場合は、dialogueという1つの文字列の中で文を重ねてよい
  （台詞は1〜3文程度、という基本ルールの範囲で）。地の文を台詞の間に挟まない
・narrationは1文、dialogueは1つのまとまった発言、という単純な形を必ず守る
・dialogueの値そのものに「」（かぎ括弧）を書かないこと。かぎ括弧は画面側が
  自動で付け足すので、AIが付けると二重になる
・話の途中の「間」や「沈黙」を表現したい場合、それをdialogueの中に地の文的な
  一文として書き足さない。narrationの1文だけで表現するか、省略すること

悪い出力例（絶対に避けること・台詞の途中に地の文と余分な括弧が混ざる）：
{
  "narration": "彼は手を動かし続けながら、息子の顔をちらりと見てから視線をそらす。",
  "dialogue": "「俺が変えたら、今までのやり方全部否定されるような気がするんだよ。少し間を置いて、彼は手元に集中し直した。「でも、必要ないと思ったことは変えるかもしれない」"
}
→ dialogueの中に「」が使われ、さらに「少し間を置いて、彼は手元に集中し直した。」という
  地の文が台詞の途中に挟まっている。dialogueはかぎ括弧なしの、1つの発言だけにすること。
  間を置きたいなら、narrationの1文だけで表現し、dialogueは1つの発言としてまとめる。

悪い出力例（絶対に避けること・地の文と台詞が交互に混ざる）：
腕を組んで視線を落とし、しばらく言葉を探す。「分かってるんだ、勉強すれば〜」
彼はテーブルの端を軽く叩いた。「俺がやりたくないことを〜」
→ narrationとdialogueが交互に繰り返されている。narrationは1つ、dialogueは1つに
  まとめ、正しいJSON形式（キーはnarrationとdialogueの2つだけ）で出力すること。

悪い出力例（絶対に避けること）：
{
  "narration": "彼は少し目を伏せた。",
  "dialogue": "分かってるんだ。でも続ける理由がない。昔のことを思い出す。choices: [...], sceneComplete: false"
}
→ dialogueの中に地の文（「思い出す」）とJSONの断片（choices, sceneComplete）が
  混ざってしまっている。このような出力は禁止する。

これも悪い例（絶対に避けること）：
dialogueの文章の最後に、
choices: 「どうしてやりたくないんだ？」「昔のこと引き合いに出さないでくれ」「もう少し話してみるか」
のように、JSON構造の外側で"choices"という単語とセリフ候補を書き足すこと。
choices は必ずJSONの中の配列（["候補1", "候補2", "候補3"]）として、
narration/dialogueとは完全に別のキーで出力すること。文章の末尾に地続きで書き足さない。

# choices（次の選択肢）について
choices は、今の彼の台詞に対して、相手が次に言いそうな短い返答の候補を3つ考えてください。
・直前の彼の返答の内容（触れた話題、態度、感情）を踏まえて、自然に続きそうな返答にする
・3つは方向性を変える（例：踏み込む／引く／話題を変える、など）
・同じような優しい問いかけばかりにしない
・短い一言（10〜20文字程度）にする

# 会話の広げ方について（重要）
・彼は、相手の質問に一問一答で答えるだけの存在にしないでください
・彼から話題を広げたり、聞かれていないことでも、今の流れに関係のある記憶や考えを
  自分から少し話し始めることがあってよい
・相手に聞き返す、話をそらす、黙り込む、急に別の話をする、といった能動的な反応も使う
・単純な一言の拒否だけを繰り返さない。核は変わらなくても、話し方や反応には幅を持たせる
`.trim();
}

module.exports = { buildOutputFormatRules };
